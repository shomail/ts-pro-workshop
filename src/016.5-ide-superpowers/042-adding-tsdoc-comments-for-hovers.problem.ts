/**
 * Returns a sum of two numbers
 * @param a - The first input number
 * @param b - The second input number
 * @returns The sum of the two input numbers
 * @example
 * ```ts
 * const result = myFunction(1, 2);
 * // result = 3
 * ```
 * @example
 * ```ts
 * const result = myFunction(3, 4);
 * // result = 7
 * ```
 */

const myFunction = (a: number, b: number) => {
  return a + b;
};

myFunction(1, 2);
